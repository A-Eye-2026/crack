def normalize_region_name(address: str) -> str | None:
    if not address:
        return None

    text = str(address).strip()

    region_alias_map = {
        "서울": "서울특별시",
        "서울시": "서울특별시",
        "서울특별시": "서울특별시",

        "부산": "부산광역시",
        "부산시": "부산광역시",
        "부산광역시": "부산광역시",

        "대구": "대구광역시",
        "대구시": "대구광역시",
        "대구광역시": "대구광역시",

        "인천": "인천광역시",
        "인천시": "인천광역시",
        "인천광역시": "인천광역시",

        "광주": "광주광역시",
        "광주시": "광주광역시",
        "광주광역시": "광주광역시",

        "대전": "대전광역시",
        "대전시": "대전광역시",
        "대전광역시": "대전광역시",

        "울산": "울산광역시",
        "울산시": "울산광역시",
        "울산광역시": "울산광역시",

        "세종": "세종특별자치시",
        "세종시": "세종특별자치시",
        "세종특별자치시": "세종특별자치시",

        "경기": "경기도",
        "경기도": "경기도",

        "강원": "강원특별자치도",
        "강원도": "강원특별자치도",
        "강원특별자치도": "강원특별자치도",

        "충북": "충청북도",
        "충청북도": "충청북도",

        "충남": "충청남도",
        "충청남도": "충청남도",

        "전북": "전북특별자치도",
        "전라북도": "전북특별자치도",
        "전북특별자치도": "전북특별자치도",

        "전남": "전라남도",
        "전라남도": "전라남도",

        "경북": "경상북도",
        "경상북도": "경상북도",

        "경남": "경상남도",
        "경상남도": "경상남도",

        "제주": "제주특별자치도",
        "제주도": "제주특별자치도",
        "제주특별자치도": "제주특별자치도",
    }

    # 1) 공백 기준 첫 토큰 우선 확인
    first_token = text.split()[0] if text.split() else text
    if first_token in region_alias_map:
        return region_alias_map[first_token]

    # 2) 주소 앞부분이 지역명으로 시작하는지 확인
    for alias, normalized in region_alias_map.items():
        if text.startswith(alias):
            return normalized

    # 3) 도/시가 빠진 주소 일부 예외 처리
    city_to_province_map = {
        "수원": "경기도",
        "성남": "경기도",
        "용인": "경기도",
        "고양": "경기도",
        "안양": "경기도",
        "부천": "경기도",
        "화성": "경기도",
        "춘천": "강원특별자치도",
        "원주": "강원특별자치도",
        "강릉": "강원특별자치도",
        "청주": "충청북도",
        "천안": "충청남도",
        "전주": "전북특별자치도",
        "목포": "전라남도",
        "포항": "경상북도",
        "창원": "경상남도",
        "제주": "제주특별자치도",
    }

    for city, province in city_to_province_map.items():
        if text.startswith(city):
            return province

    return None